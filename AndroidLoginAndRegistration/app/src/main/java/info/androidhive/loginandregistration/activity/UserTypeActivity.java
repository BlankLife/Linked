public class UserTypeActivity extends Activity{
    private Button btnRegUser;
    private Button btnOwnerUser;
    private ProgressDialog pDialog;
    private SessionManager session;
    private SQLiteHandler db;

    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_user_type);

        btnRegUser = (Button) findViewById(R.id.btnRegUser);
        btnOwnerUser = (Button) findViewById(R.id.btnOwnerUser);

        // Progress dialog
        pDialog = new ProgressDialog(this);
        pDialog.setCancelable(false);

        // Session manager
        session = new SessionManager(getApplicationContext());

        // SQLite database handler
        db = new SQLiteHandler(getApplicationContext());

        // Check if user is already logged in or not
        if (session.isLoggedIn()) {
            // User is already logged in. Take him to main activity
            Intent intent = new Intent(UserTypeActivity.this,
                    MainActivity.class);
            startActivity(intent);
            finish();
        }

        // Link to Reg Register Screen
        btnRegUser.setOnClickListener(new View.OnClickListener() {

            public void onClick(View view) {
                Intent i = new Intent(getApplicationContext(),
                        RegisterActivity.class);
                startActivity(i);
                finish();
            }
        });

        // Link to Owner Register Screen
        btnOwnerUser.setOnClickListener(new View.OnClickListener() {

            public void onClick(View view) {
                Intent i = new Intent(getApplicationContext(),
                        OwnerRegisterActivity.class);
                startActivity(i);
                finish();
            }
        });

    }

}
